    private void btnGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoActionPerformed
        // TODO add your handling code here: 
        String jugador = txtNombre.getText();  
        if(jugador.equals("")){ 
             JOptionPane.showMessageDialog(null, "Escoja nombre de usuario v√°lido.");
        }
        else{
            try {
                panel = jchat.asignarPanel(jugador);
                System.out.println("Panel asignado: "  +panel);
                System.out.println("Avatar asignado: " +avatar);
                if(!jchat.iniciarSesion(jugador, panel, avatar)){
                     JOptionPane.showMessageDialog(null, "Nombre ya existe.\nEscriba otro Nombre.");
                }
                else{
                    juego = new ventanaJuego(jugador, jchat, avatar);  
                    juego.setVisible(true); 
                    this.dispose();
                }
            } catch (RemoteException ex) {
            Logger.getLogger(ventanaInicio.class.getName()).log(Level.SEVERE, null, ex);
          }
        }
    }//GEN-LAST:event_btnGoActionPerformed

